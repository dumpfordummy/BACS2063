    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed

        if (nameField.getText().equals("")) {
            resultMessage.setText("Name cannot be empty");
            return;
        }
        if (studentIdField.getText().equals("")) {
            resultMessage.setText("Student Id cannot be empty");
            return;
        }
        if (emailField.getText().equals("")) {
            resultMessage.setText("Email cannot be empty");
            return;
        }
        
        Student student = new Student(nameField.getText(), studentIdField.getText(), emailField.getText());
        
        TutorialGroup tutGroup = tutorialGroupController.findTutorialGroupByGroupNumber(Integer.parseInt((String) tutorialGroupField.getSelectedItem()));
        
        tutGroup.getStudentList().add(student);
        
        tutGroup.incrementNoOfStudents();
        
        resultMessage.setText("Successfully added " + student.getStudID() + " to tutorial group " + tutGroup.getGroupNumber());
        
        clearInputField();
        
        
    }//GEN-LAST:event_addBtnActionPerformed

    private void clearInputField() {
        nameField.setText("");
        studentIdField.setText("");
        emailField.setText("");
        tutorialGroupField.setSelectedIndex(0);
    }

